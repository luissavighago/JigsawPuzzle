package puzzle.storeage;

public class JigsawPuzzleException extends Exception {
	
	public JigsawPuzzleException() {
		super();
	}
	
	public JigsawPuzzleException(Throwable t) {
		super(t);
	}
	
	public JigsawPuzzleException(String message) {
		super(message);
	}
	
	public JigsawPuzzleException(String message, Throwable t) {
		super(message, t);
	}

}
